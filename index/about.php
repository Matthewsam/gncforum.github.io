          <h2 class="display-5 fw-bold">Guru Nanak College</h2>
          <p>The Guru Nanak College, (GNC) in a lush green 25-acre campus, cradled between the Raj Bhavan and the campus of the IIT-Madras, facing the Guru Nanak Salai, is located in Velachery, South Chennai &ndash; the gateway to the IT expressway of Tamil Nadu.</p>

          <p>Guru Nanak College was established in 1971 to commemorate the 500th birth anniversary of Shri Guru Nanak Devji &ndash; the founder of the Sikh religion, by the Guru Nanak Educational Society (GNES). (Late) Lt. Col. G.S. Gill was the founder President, (Late) Padmashree P. N. Dhawan was the General Secretary and (Late) Sardar Manohar Singh Sabherwal was the Treasurer. This yeoman service of a small community of Sikhs, who made Tamil Nadu their home, after they left Punjab following the partition is historic.</p>

          <p>The noble mission of the GNES is to provide quality education to all, irrespective of socio-economic background, caste, creed or religion. This is in keeping with the motto of the College 'Pro Bono Publico' which is a translation of Sikh prayer &lsquo;Sarbat ka bhalla&rsquo; meaning for the 'Benefit of All'. The Sikhs are a minority in the State and yet the Management does not claim any concessions extended to minority institutions. It is run and regulated like a non-minority institution in service of the majority community</p>

